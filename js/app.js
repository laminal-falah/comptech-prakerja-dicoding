const MenuLanguage = () => {
  const lang = Lang();
  const listLang = document.body.querySelector('#lang');
  listLang.innerHTML = lang.toUpperCase();
  const childLang = listLang.nextSibling.nextSibling.children;
  for (let i = 0; i < childLang.length; i++) {
    if (childLang[i].children[0].id == lang) {
      childLang[i].children[0].classList.toggle('active');
    } else {
      childLang[i].children[0].addEventListener('click', function(e) {
        setLocalService('lang', childLang[i].children[0].id);
        window.location.reload();
      });
      childLang[i].children[0].classList.toggle('active', false);
    }
  }
}

const Header = () => {
  const lang = Lang();

  //Data("../data/data.json", data => {
    const header = data.header;
    const topbar = header.topbar;
    const navbar = header.navbar;

    const topbarElement = document.body.querySelector(".topbar-nav");
    const navbarTitle = document.body.querySelector(".navbar-title");
    const navbarElement = document.getElementById("menu");

    Helper.removeChild(topbarElement);
    Helper.removeChild(navbarElement);

    navbarTitle.innerHTML = header.name;

    topbar.map((v, k) => {
      const li = document.createElement("li");
      const a = document.createElement("a");
      a.className = 'topbar-link';
      a.innerHTML = `${v.title[lang]} : ${v.content}`;
      li.appendChild(a);
      topbarElement.appendChild(li);
    });

    navbar.map((v, k) => {
      const li = document.createElement("li");
      const a = document.createElement("a");
      a.setAttribute('data-page', v.url);
      a.className = `navbar-link ${k == 0 ? 'active': ''}`;
      a.innerHTML = v.title[lang];
      a.href = `/#${v.url}`;
      li.appendChild(a);
      navbarElement.appendChild(li);
    });

    const dataPage = document.querySelectorAll("a[data-page]");

    dataPage.forEach((link) => {
      link.addEventListener('click', (e) => {
        e.preventDefault();

        dataPage.forEach((link) => {
          if (link.classList.contains('active')) {
            link.classList.toggle('active', false);
          }
        });

        link.classList.toggle('active', true);
        const page = link.getAttribute('data-page');
        window.scroll({
          behavior: 'smooth',
          left: 0,
          top: (page === '/') ? 0 : document.getElementById(page).offsetTop - 50
        });
      });
    });
  //});

  MenuLanguage();
  Collapse();
  Dropdown();
  Helper.outside();
}

