const Helper = {
  dropdown: (toggle) => {
    const COLLAPSED = 'collapsed';
    const SELECTOR_DATA_TOGGLE = toggle;
    const dropdown = document.body.querySelectorAll(SELECTOR_DATA_TOGGLE);
    dropdown.forEach((v, k) => {
      v.addEventListener('click', (e) => {
        e.preventDefault();
        e.stopPropagation();

        v.classList.toggle(COLLAPSED);
        const isOpen = v.classList.contains(COLLAPSED);
        v.setAttribute('aria-expanded', isOpen);
        const element = document.body.querySelector(v.getAttribute('data-target'));
        element.animate([
          {
            opacity: 0,
          },
          {
            opacity: 1,
          }
        ], 250);
        element.classList.toggle('show', isOpen);
      });
    });
  },
  outside: () => {
    document.addEventListener('mouseup', (e) => {
      e.preventDefault();
      e.stopPropagation();

      const show = document.body.querySelectorAll(".show");

      if (show.length > 0) {
        show.forEach((v, k) => {
          if (v.classList.contains('navbar-collapse')) {
            return true;
          } else {
            const collapse = document.body.querySelectorAll(".collapsed");
            collapse.forEach((v, k) => {
              v.classList.remove('collapsed');
              v.setAttribute('aria-expanded', false);
              v.nextElementSibling.classList.remove('show');
            });
          }
        });
      } else {
        return true;
      }
    });
  },
  sticky: () => {
    const topBar = document.body.querySelector('.topbar');
    const background = document.body.querySelector('.welcome-background');
    const navbar = document.body.querySelector('nav');
    const arrowUp = document.body.querySelector('.scroll-up');

    let clientHeight = document.documentElement.scrollTop;

    if (clientHeight > topBar.clientHeight + 5) {
      topBar.classList.toggle('topbar-none', true);
      navbar.classList.toggle('navbar-mini', true);
      arrowUp.classList.toggle('show', true);
      background.style.height = '92vh';
    } else {
      topBar.classList.toggle('topbar-none', false);
      navbar.classList.toggle('navbar-mini', false);
      arrowUp.classList.toggle('show', false);
      background.style.height = '90vh';
    }

    const dataPage = document.querySelectorAll("a[data-page]");

    if (dataPage !== null) {
      dataPage.forEach((link) => {
        const page = link.getAttribute('data-page');
        if (page === '/') {
          link.classList.toggle('active', true);
        } else {
          if (document.documentElement.scrollTop >= document.getElementById(page).offsetTop - 50) {
            dataPage.forEach((link) => {
              if (link.classList.contains('active')) {
                link.classList.toggle('active', false);
              }
            });
            link.classList.toggle('active', true);
          } else {
            link.classList.toggle('active', false);
          }
        }
      });
    }
  },
  scrollUp: () => {
    const arrowUp = document.body.querySelector('.scroll-up');
    arrowUp.addEventListener('click', (e) => {
      e.preventDefault();
      e.stopPropagation();
      document.body.scrollIntoView({ behavior: 'smooth', block: 'start'});
    });
  },
  removeChild: (element) => {
    if (element.children.length > 0) {
      for (let i = element.children.length - 1; i >= 0; i--) {
        element.children[i].remove();
      }
    }
  }
}

const Collapse = () => Helper.dropdown('[data-toggle="collapse"]');

const Dropdown = () => Helper.dropdown('[data-toggle="dropdown"]');

const Lang = () => {
  const defaultLang = 'id';
  const lang = ['en', 'id'];

  if (getLocalService('lang') == null) {
    setLocalService('lang', defaultLang);
  }

  return lang.filter(v => v === getLocalService('lang'))[0] || defaultLang;
}

